#!/usr/bin/python3
"""This module provides a function that returns the sum of two float numbers."""

def add(a: float, b: float) -> float:
    """
    Add two float numbers and return the result as a float.

    Args:
        a (float): The first float number to add.
        b (float): The second float number to add.

    Returns:
        float: The result of the addition.
    """

    return a + b
